let a = prompt("Enter number a...")
let b = prompt("Enter number b...")
if(a>b){
    alert(`${a} > ${b}`)
}
else if(a<b){
    alert(`${a} < ${b}`)
}
else if(a==b){
    alert(`${a} == ${b}`)
}
else{
    alert("Wrong Number")
}
